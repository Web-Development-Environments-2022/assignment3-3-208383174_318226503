      required: true,
    },
  },
  data() {
    return {
        max: 0,
        value: 0
    }
  },
  created() {
    this.max=5;
    this.value = this.get_Session_Value();
    console.log("recipe progress bar this.value : "+this.value);
    console.log(this.r_id);
  },
  mounted() {


  },
  methods: {
     get_Session_Value(){
      return JSON.parse(sessionStorage.making_progress)[this.r_id];
    },
  },
}
